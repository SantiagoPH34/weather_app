import 'package:weather_app/features/weather/data/models/hour_model.dart';
import 'package:weather_app/features/weather/domain/entities/day.dart';
import 'package:weather_app/features/weather/domain/entities/hour.dart';

class DayModel extends Day {
  DayModel({
    required super.datetime,
    required super.datetimeEpoch,
    required super.tempmax,
    required super.tempmin,
    required super.temp,
    required super.feelslikemax,
    required super.feelslikemin,
    required super.feelslike,
    required super.dew,
    required super.humidity,
    required super.precip,
    required super.precipprob,
    required super.precipcover,
    required super.preciptype,
    required super.snow,
    required super.snowdepth,
    required super.windgust,
    required super.windspeed,
    required super.winddir,
    required super.pressure,
    required super.cloudcover,
    required super.visibility,
    required super.solarradiation,
    required super.solarenergy,
    required super.uvindex,
    required super.severerisk,
    required super.sunrise,
    required super.sunriseEpoch,
    required super.sunset,
    required super.sunsetEpoch,
    required super.moonphase,
    required super.conditions,
    required super.description,
    required super.icon,
    required super.stations,
    required super.source,
    required super.hours,
  });

  factory DayModel.fromJson(Map<String, dynamic> json) => DayModel(
    datetime: DateTime.parse(json["datetime"]),
    datetimeEpoch: json["datetimeEpoch"],
    tempmax: json["tempmax"]?.toDouble(),
    tempmin: json["tempmin"],
    temp: json["temp"]?.toDouble(),
    feelslikemax: json["feelslikemax"]?.toDouble(),
    feelslikemin: json["feelslikemin"],
    feelslike: json["feelslike"]?.toDouble(),
    dew: json["dew"]?.toDouble(),
    humidity: json["humidity"]?.toDouble(),
    precip: json["precip"]?.toDouble(),
    precipprob: json["precipprob"],
    precipcover: json["precipcover"]?.toDouble(),
    preciptype: List<String>.from(json["preciptype"].map((x) => x)),
    snow: json["snow"],
    snowdepth: json["snowdepth"],
    windgust: json["windgust"]?.toDouble(),
    windspeed: json["windspeed"]?.toDouble(),
    winddir: json["winddir"]?.toDouble(),
    pressure: json["pressure"]?.toDouble(),
    cloudcover: json["cloudcover"]?.toDouble(),
    visibility: json["visibility"]?.toDouble(),
    solarradiation: json["solarradiation"]?.toDouble(),
    solarenergy: json["solarenergy"]?.toDouble(),
    uvindex: json["uvindex"],
    severerisk: json["severerisk"],
    sunrise: json["sunrise"],
    sunriseEpoch: json["sunriseEpoch"],
    sunset: json["sunset"],
    sunsetEpoch: json["sunsetEpoch"],
    moonphase: json["moonphase"]?.toDouble(),
    conditions: json["conditions"],
    description: json["description"],
    icon: json["icon"],
    stations: List<String>.from(json["stations"].map((x) => x)),
    source: json["source"],
    hours: List<HourModel>.from(json["hours"].map((x) => HourModel.fromJson(x))),
  );


  @override
  Map<String, dynamic> toJson() => {
    "datetime":
        "${datetime.year.toString().padLeft(4, '0')}-${datetime.month.toString().padLeft(2, '0')}-${datetime.day.toString().padLeft(2, '0')}",
    "datetimeEpoch": datetimeEpoch,
    "tempmax": tempmax,
    "tempmin": tempmin,
    "temp": temp,
    "feelslikemax": feelslikemax,
    "feelslikemin": feelslikemin,
    "feelslike": feelslike,
    "dew": dew,
    "humidity": humidity,
    "precip": precip,
    "precipprob": precipprob,
    "precipcover": precipcover,
    "preciptype": List<dynamic>.from(preciptype.map((x) => x)),
    "snow": snow,
    "snowdepth": snowdepth,
    "windgust": windgust,
    "windspeed": windspeed,
    "winddir": winddir,
    "pressure": pressure,
    "cloudcover": cloudcover,
    "visibility": visibility,
    "solarradiation": solarradiation,
    "solarenergy": solarenergy,
    "uvindex": uvindex,
    "severerisk": severerisk,
    "sunrise": sunrise,
    "sunriseEpoch": sunriseEpoch,
    "sunset": sunset,
    "sunsetEpoch": sunsetEpoch,
    "moonphase": moonphase,
    "conditions": conditions,
    "description": description,
    "icon": icon,
    "stations": List<dynamic>.from(stations.map((x) => x)),
    "source": source,
    "hours": List<dynamic>.from(hours.map((x) => x.toJson())),
  };
}
